
public class Room {
	
	private int roomid;
	private int coverage;
	
	
	public Room(int roomid, int coverage) {
		this.roomid = roomid;
		this.coverage = coverage;
		
	}
	public int getRoomid() {
		return roomid;
	}
	public void setRoomid(int roomid) {
		this.roomid = roomid;
	}
	public int getCoverage() {
		return coverage;
	}
	public void setCoverage(int coverage) {
		this.coverage = coverage;                                                                                                                                                                                                                                                
	}
	
	
}
